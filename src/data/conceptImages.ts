/** 计算所有 concept image 的 URL 列表，用于批量预热 */
export function allConceptImageUrls(): string[] {
  return CONCEPT_IMAGES.map((c) =>
    textToImageUrl(c.prompt + ", masterpiece, best quality, 4k", "portrait_4_3"),
  );
}

/** 按分类统计 */
export function conceptImageStats() {
  const byCategory: Record<string, number> = {};
  const byGame: Record<string, number> = {};
  for (const c of CONCEPT_IMAGES) {
    byCategory[c.category] = (byCategory[c.category] || 0) + 1;
    byGame[c.game] = (byGame[c.game] || 0) + 1;
  }
  return { byCategory, byGame, total: CONCEPT_IMAGES.length };
}
