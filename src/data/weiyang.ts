export function findNodeById(nodeId: string): { dimension: Dimension; section: Section } | null {
  for (const dim of dimensions) {
    for (const sec of dim.sections) {
      if (sec.id === nodeId) {
        return { dimension: dim, section: sec };
      }
    }
  }
  return null;
}

export function getAllNodeIds(): string[] {
  const ids: string[] = [];
  for (const dim of dimensions) {
    for (const sec of dim.sections) {
      ids.push(sec.id);
    }
  }
  return ids;
}
