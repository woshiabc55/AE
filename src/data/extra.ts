function getDisplay(name: string): string {
  return name
    .split('-')
    .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
    .join(' ');
}

// 公共图标按源格式化（输出格式：line/filled）
type AnyIconDef = { name: string; displayName: string; category: IconCategory; tags: string[]; d: string };
function makeForCategory(cat: IconCategory, source: 'line' | 'filled'): AnyIconDef[] {
  return COMMON_TYPES[cat].map((name) => {
    const baseD = D_PATHS[name] || 'M12 12h.01';
    return {
      name,
      displayName: getDisplay(name),
      category: cat,
      tags: [name.split('-')[0]],
      d: baseD,
    };
  });
}

