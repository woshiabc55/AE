const CHAR_TO_INDEX: Record<string, number> = {
  ".": 0,
  "#": 1, // line
  "0": 1,
  "1": 1, // 描边（深色）用 1
  "2": 2, // skin
  "3": 3, // sakura / 头发
  "4": 4, // butter
  "5": 6, // sky（眼睛高光）
  "6": 9, // white
  "7": 11, // ink-dark
  "8": 7, // flame
  "9": 5, // leaf
};

/** 构造示例：萌系少女 */
const buildSample = (): PixelCanvas => {
  const data: number[] = new Array(W * H).fill(0);
  for (let y = 0; y < ASCII.length && y < H; y++) {
    const row = ASCII[y];
    for (let x = 0; x < row.length && x < W; x++) {
      const c = row[x];
      const ci = CHAR_TO_INDEX[c] ?? 0;
      data[y * W + x] = ci;
    }
  }
  return {
    width: W,
    height: H,
    palette: [...DEFAULT_PALETTE],
    data,
    currentColor: 2,
  };
};

