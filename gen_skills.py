def gen_id(s):
    return hashlib.md5(s.encode("utf-8")).hexdigest()[:8]

def gen_one(idx):
    cat = random.choice(list(CATEGORY_CORES.keys()))
    core = random.choice(CATEGORY_CORES[cat])
    prefix = random.choice(PREFIXES)
    name = f"{prefix}{core}".strip()
    # 同一组合去重
    return {
        "id": gen_id(f"{idx}-{name}"),
        "index": idx,
        "name": name,
        "category": cat,
        "level": random.choice(LEVELS),
        "description": random.choice(DESCRIPTIONS),
        "hot": random.randint(40, 99),
    }

# 生成时保证不重名
seen = set()
items = []
i = 1
while len(items) < 240:
    item = gen_one(i)
    if item["name"] in seen:
        i += 1
        continue
    seen.add(item["name"])
    items.append(item)
    i += 1

# 写入 JSON
with open("/workspace/skills.json", "w", encoding="utf-8") as f:
    json.dump({"categories": CATEGORIES, "items": items}, f, ensure_ascii=False, indent=2)

